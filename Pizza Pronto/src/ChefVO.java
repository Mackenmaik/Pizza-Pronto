
public class ChefVO {
	
	static int a;
	static int b;
	static int c;
	
	public ChefVO(int a, int b, int c) {
		super();
		ChefVO.a = a;
		ChefVO.b = b;
		ChefVO.c = c;
	}

	public int getA() {
		return a;
	}

	public void setA(int a) {
		ChefVO.a = 5;
	}

	public int getB() {
		return b;
	}

	public void setB(int b) {
		ChefVO.b = 5;
	}

	public int getC() {
		return c;
	}

	public void setC(int c) {
		ChefVO.c = 5;
	}
	
	
	
	

}
